public class save_user_information {

    public String name;
    public String email;
    public String mobile;
    public String h_no;
    public String r_no;

    public save_user_information(){

    }

    public save_user_information(String name, String email, String mobile, String h_no, String r_no) {
        this.name = name;
        this.email = email;
        this.mobile = mobile;
        this.h_no = h_no;
        this.r_no = r_no;
    }
}
